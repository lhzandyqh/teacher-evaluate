      tableData: [],
      bisaiType: [{ label: '论文案例', value: 'lunwen' }, { label: '现场课', value: 'xianchang' }, { label: '说课', value: 'shuoke' }, { label: '基本功', value: 'jiben' }, { label: '其他', value: 'qita' }],
